//: ## Adlar ve Otomatik Tamamlama
//: Xcode, tanımladığınız bütün adları takip eder. Böylece; siz bir adı kullanmak için yazmaya başladığınızda, önerilen adlardan (yani bilinen adlardan) oluşan bir menü göreceksiniz. Buna _otomatik tamamlama_ diyoruz.
//:
//: Otomatik tamamlama, kodunuzu daha az yazım hatası içerecek şekilde ve daha hızlı yazabilmenizi sağlar. Siz yazdıkça, otomatik tamamlama menüsündeki öneriler aradığınız şeye göre daralır.
//:
//: Otomatik tamamlama menüsü belirdiğinde yapabilecekleriniz:
//: - Listeyi daraltmak için yazmaya devam edebilirsiniz.
//: - Yukarı ok ve aşağı ok tuşlarını kullanarak listede gezinebilirsiniz.
//: - Seçtiğiniz öneriyi Xcode'un doldurması için Enter tuşuna basabilirsiniz.
//:
//: ![Otomatik Tamamlama Açılır Menüsü](autocomplete.png)
//: Listede, tanımlamadığınız ve size tanıdık gelmeyen isimler de göreceksiniz, onları görmezden gelebilirsiniz.
//:
//:\
//: Sonraki sayfadaki deneyin bir parçası olarak, birkaç isim yazacak ve otomatik tamamlamayı iş başında göreceksiniz.
