//: ## Evcil Hayvan Problemi 🐶
//: Arkadaşınızın bir evcil hayvan gösterisi düzenleyeceğini ve gösteride her hayvandan kaç adet olacağını takip etmesi gerektiğini hayal edin.
//: Arkadaşınız, aynı zamanda gösterideki toplam hayvan sayısını ve bunların kaçının memeli hayvan sınıfına dahil olduğunu da takip etmeli.
//:
//: Şimdi yanınızda sadece Xcode yüklü bir dizüstü bilgisayar olduğunu düşünün. Belki de arkadaşınıza yardım etmek için bir Playground sayfası kullanabilirsiniz!
//:
//: Şu ana kadar öğrendiklerinizi kullanarak, her hayvanın sayısını bir yorum satırı ve sayı ile takip edebilirsiniz:
// Köpeklerin Sayısı
5

// Kedilerin Sayısı
4

// Kaplumbağaların Sayısı
3
//: Sonrasında bu sayıları yeni kod satırlarına kopyalayabilir ve Playground ortamının sizin için hesaplama yapmasını sağlayabilirsiniz:
// Toplam Hayvan Sayısı
5 + 4 + 3

// Toplam Memeli Sayısı
5 + 4
//: - callout(Deney): Etkinlik günü yaklaştıkça, gösteride hangi hayvanların olacağı ile ilgili daha fazla bilgi edinmeye başladınız.\
//:Toplam hayvan sayısını güncellemek için yukarıdaki kodu değiştirin.\
//:
//: Örneğin; Katılımcılardan biri, yanında bir köpek getiriyorsa, `Köpeklerin Sayısı`nı `5`'ten `6`'ya değiştirin. `5` sayısını kullandığımız diğer her yerde de (`Hayvanların Toplamı` ve `Memelilerin Toplamı`) `5` sayısını `6` ile değiştirin.\
//:
//: Eğer bir kişinin yanında getireceği hayvan daha önce kimse tarafından belirtilmediyse,  diğer hayvan sayılarını yazdığımız şekilde onu da ekleyin. Yeni bir kod satırında (`// Hamster'ların Sayısı`) gibi bir açıklama ve sonraki satıra da bir sayı yazın. Toplamları güncellemeyi unutmayın.
//:
//: - Başka birisi de bir köpek getiriyor.
//: - Kaplumbağalardan biri hasta ve gelemeyecek. 😥
//: - Bir kişi daha kedi getiriyor.
//: - Birisi de hamster'ı ile katılmaya karar verdi (bir memeli).
//:
