/*:
 ## Egzersiz: Else (Değilse) Alıştırmaları
 
 Bir `if` ifadesi doğru olmadığında çalışan kodların `else` ifadeleri tarafından tutulduğunu hatırlayabilirsiniz. Bu ifadeler, bir programın akışının ne olması gerektiğini daha açık hale getirirler, ve gereken kodu azaltabilirler.
*/
let gizliKelime = "elma"
let tahmin = "armut"

if gizliKelime == tahmin {
    "Tahmininiz doğru."
}
if gizliKelime != tahmin {
    "Tahmininiz yanlış."
}
//: - callout(Egzersiz): Yukarıdaki kodu `else` ifadeleri kullanacak şekilde yeniden yazın.
