import UIKit
import PlaygroundSupport
/*:
 ## Footer Oluşturmak
 ![Footer Components](instagram-parts-footer-components.pdf "Footer Components")
 
 - Callout(Yapılması Gerekenler):
    - _footer_ adında bir _Rectangle_ oluşturun.
    - Arkaplan rengini beyaz olarak ayarlayın.
    - Dikdörtgenin boyutunu (width: 375, height: 75) olarak belirtin.
    - Oluşturduğunuz diktörtgeni Playground'un _liveView_ özelliğini kullanarak çizilmesini sağlayın.
*/
// Kodlama Alanı





/*:
  ![Like Count](instagram-parts-footer-components-likecount.pdf "Like Count")
    Beğeniler, Instagram'ın olmazsa olmazıdır. Biz geliştiriciler için durum farklı değil. Öyleyse gönderimizin kaç kişi tarafından beğenildiğini gösterecek bir _Text_ arayüz elemanı oluşturarak işe koyulalım.
 - Callout(Yapılması Gerekenler):
    - _likeCount_ adında bir _Int_ sabiti tanımlayın.
    - _likeCountText_ adında bir _Text_ oluşturun.
    - Beğeni sayısı metnini, (xxx _likeCount_) formatında belirtin.
    - Yazı rengini siyah yapın.
    - Yazı fontunu (14, semibold) yapın.
    - Boyutu  (width: 345, height: 15) olarak belirtin.
    - Pozisyonu (x: 15 y: 0) olarak belirtin.
    - Oluşturduğunuz elemanı _footer_'a ekleyin.
*/
// Kodlama Alanı










/*:
  ![Content](instagram-parts-footer-components-content.pdf "Content")
 Sizi bilmeyiz ama biz TurkishKit olarak tüm gönderilerimize içerik yazmayı çok seviyoruz! Şimdi sıra içerik metnimizi gösterecek olan arayüz elemanında.
 - Callout(Yapılması Gerekenler):
    - _contentText_ adında bir _Text_ oluşturun.
    - Metninizi belirtin.
    - Yazı rengini siyah yapın.
    - Yazı fontunu (14, regular) yapın.
    - Boyutu  (width: 345, height: 20) olarak belirtin.
    - Pozisyonu (x: 15 y: likeCountText.bottom) olarak belirtin.
    - Oluşturduğunuz elemanı _footer_'a ekleyin.
*/
// Kodlama Alanı









/*:
  ![Comment Count](instagram-parts-footer-components-commentcount.pdf "Comment Count")
 Beğeni sayısı gibi yorum sayısı da oldukça önemli. Bir çekiliş yapıyorsanız kaç kişinin çekilişinize katıldığını bilmek istersiniz. Benzersiz gönderimizin daha büyük kitlelere ulaşması yorumlara bağlı!
 - Callout(Yapılması Gerekenler):
    - _commentCount_ adında bir _Int_ sabiti tanımlayın.
    - _commentCountText_ adında bir _Text_ oluşturun.
    - Yorum sayısı metnini, (View all _commentCount_ comments) formatında belirtin.
    - Yazı rengini (instaGray) yapın.
    - Yazı fontunu (14, medium) yapın.
    - Boyutu  (width: 355, height: 15) olarak belirtin.
    - Pozisyonu (x: 15 y: contentText.bottom + 5) olarak belirtin.
    - Oluşturduğunuz elemanı _footer_'a ekleyin.
*/
// Kodlama Alanı










