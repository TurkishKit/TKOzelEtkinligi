 Eğer değer 3 ise, bir sorun olmaz:
 
 `Videonun uzunluğu 3 saniye. Gözümü kırpsaydım, hepsini kaçırırdım!`

 Ancak, videonun uzunluğunu `2857013857` gibi çok büyük bir değer ile değiştirmeyi deneyin. Bu durumda, `videoMesaji` pek doğru gözükmeyecektir:
 
 `Videonun uzunluğu 2857013857 saniye. Gözümü kırpsaydım, hepsini kaçırırdım!`
 
 Değişkenin değerine göre, kodunuzun farklı şeyler yapmasını istiyorsunuz. Kodunuzun kararlar almasına ihtiyacınız var.
 
 
 
