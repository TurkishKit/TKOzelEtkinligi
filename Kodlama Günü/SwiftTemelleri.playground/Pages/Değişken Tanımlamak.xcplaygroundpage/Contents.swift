/*:
 ## Değişken Tanımlamak
 
 Bir değişken tanımlamak, bir sabit tanımlamaya çok benzer. Aradaki tek fark, `let` anahtar kelimesi yerine `var` anahtar kelimesini kullanmanızdır:
*/
// Bir sabit tanımlamak
let dogumYeri = "Ankara"

// Bir değişken tanımlamak
var mevcutKonum = "İstanbul"

/*:
 Bir değişken tanımladığınızda, ona yeni bir değer atayabilirsiniz:
*/
// Assigning a new value to a variable
mevcutKonum = "Levent 199"

/*:
 - callout(Deney): Aşağıdaki alana, `mevcutKonum` değişkenine yeni bir değer atayan bir kod satırı ekleyin:
 */
// Yeni bir değer atamayı deneyin:
// mevcutKonum = "Hayalinizdeki yer"
/*:
 Sabitler ve değişkenler, çoğu noktada benzerdir:
 - Her ikisi de bir ismi bir değer ile bağdaştırır.
 - Her ikisi de isimlendirme için aynı kurallara sahiptirler.
 - Her ikisi de belirli bir türe sahiptir.
 
 
 \
 Ancak, aralarında bazı farklılıklarda bulunur:
 - Bir sabitin değeri, belirlendikten sonra *değiştirilemez*.
 - Bir değişkenin değeri, belirlendikten sonra *değiştirilebilir*.
 */
