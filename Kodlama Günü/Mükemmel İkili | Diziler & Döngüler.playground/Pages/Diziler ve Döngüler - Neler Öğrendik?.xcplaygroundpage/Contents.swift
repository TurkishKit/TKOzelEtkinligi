 ## Özetle
 
 Ögelerin bir listesini tutmak için, dizileri kullanabilirsiniz. Diziler, oldukça önemli iki özelliğe sahiptir:
 
 - Dizideki bütün ögeler, aynı türdendir.
 - Dizideki ögeler, belirli bir sıraya sahiptir.
 
 Bu iki özellik sayesinde, dizini (index) kullanarak dizi üzerindeki noktalardan ögelere erişebilirsiniz, ve her zaman türü bilinen bir değer elde edersiniz.
 
 İşte, diziler hakkında öğrendiğimiz diğer şeyler:
 
 - Bir dizinin ilk dizini, bir değil, sıfırdır..
 - Dizini kullanarak dizilere erişmek, tehlikeli olabilir. Eğer dizin, ögelerin bulunduğu aralığın dışında ise, programınız çökecektir.
 - Bir dizideki ögelerin sayısını, `count` özelliğini kullanarak bulabilirsiniz.
 - Dizide kaç öge bulunduğunu bilmenize gerek olmadan, her ögeye sırasıyla erişmek için, `for…in` döngüleri kullanabilirsiniz.
 - Değişken diziler, ögeleri eklemenizi, çıkarmanızı ve değiştirebilmenizi sağlar.
 
 Şimdi, egzersizler ile öğrendiklerimizi pekiştirelim!

