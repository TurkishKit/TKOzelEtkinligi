 ## Listeler
 
 İnsanlar, listeleri sürekli kullanır. Bir yapılacaklar listeniz, dilek listeniz, Apple Music çalma listeniz, hatta ölmeden önce yapılması gerekenler listeniz olabilir. Listeler çok kullanışlıdır, ve kodlamada da yaygındır.
 
 Değişkenlerde ekleme ve çıkarmayı öğrendiğinizde, bazı egzersizlerde bir şeylerin listesini oluşturdunuz. Birden fazla karakter dizisini yeni satır karakteriyle `\n` birleştirerek listeler yaptınız.

 - callout(Örnek): `let alisverisListesi = "Yumurta" + "\n" + "Süt" + "\n" + ...`
 
 Bu kod, ekranda güzel gözüken bir liste verir, ancak bunun dışında onunla yapabileceğiniz pek bir şey yoktur. Bir listeyle yapmak isteyebileceğiniz başka şeyleri düşünün:
 
 - Hepsini tekrar yazmadan, listenin her ögesi üzerinde nasıl işlem yapabilirdiniz?
 - Listeye bir şeyi zaten ekleyip eklemediğinizi nasıl kontrol edebilirdiniz?
 - Listeniz yüzlerce maddeye genişlediğinde, "Domates" olanı kolayca çıkarabilir miydiniz?
 - Peki ya listeniz, toplamak istediğiniz fiyatların listesi gibi, başka tür elemanlardan oluşsaydı?
 - İlk öge ne? Son öge ne? 24'üncüsü?
 - Listede kaç şey var?
 - Listeyi nasıl yeniden sıralayabilirsiniz?

 Swift dilinde listeler, _diziler_ olarak bilinir.

 Bir şeylerin listesi üzerinde kaydırarak dolaştığınız herhangi bir iOS uygulamasını düşünün. O uygulama, neredeyse kesinlikle dizileri kullanıyordur.
 
 Bu oturumda, dizileri nasıl oluşturabileceğinizi ve üzerlerinde nasıl çalışabileceğinizi öğreneceğiz!

